// ̓s{ԍ𓾂
int getCastleTodouhukenNum(int iCastleID ) {
	if ( 0 <= iCastleID && iCastleID < GAMEDATASTRUCT_CASTLE_NUM ) {
		return CastleIDToTodouhukenNumTable[iCastleID];
	} else {
		return 0xFFFF;
	}
}

// s{ԍs{𓾂
char* getTodouhukenName(int iTodouhukenNum) {
	iTodouhukenNum = iTodouhukenNum - 1; // 1IW̓s{ԍ0IW̓s{z
	if ( 0 <=  iTodouhukenNum && iTodouhukenNum < sizeof(szTodouhukenNameTable)/sizeof(szTodouhukenNameTable[0]) ) {
		return szTodouhukenNameTable[iTodouhukenNum];
	}
	return "";
}



